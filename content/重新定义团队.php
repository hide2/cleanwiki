# 不要相信你的直觉
-------------------------
头10秒钟的印象使得我们在整个面试过程中都在试图证明我们对某个人的印象，而不是真正地去评估他们。心理学家将这种现象称作证实偏见，“倾向于寻找、解释或优先考虑那些能够支持我们观点或假设的信息”
=blue 【注】多数的面试都是在浪费时间，因为99.4%的时间都用在证实面试官最初10秒钟的印象
  
