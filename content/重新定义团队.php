# 管理团队的两端 — 最优员工和最差员工
-------------------------
一位前微软工程师说：“如果你的团队中有10个人，你开始工作的第一天就了解到，不管每一位员工多么优秀，都将有两个人获得好评，7个人获得中评，另外有一个人获得差评，这使得员工的注意力都放在内部互相竞争上，而不是与其他公司竞争。”
=blue 【注】坦诚相待，给员工改进的机会
  
员工不是离开公司，而是离开糟糕的经理
=blue 【注】经理对团队的绩效和开心度产生非常重要的影响
  
高分经理的8种特性：
做一名好的导师
给团队授权，不随便插手下属工作
表达出对团队成员的成功和个人幸福的兴趣和关心
高效/结果导向型
善于沟通--聆听和分享信息
在职业发展方面助力团队
对团队有清晰的愿景和战略
具备重要的技术技能，可为团队提供建议
=blue 【注】高分经理8荣8耻
  
向上反馈调查问卷样例
我的经理给我可行的反馈意见，帮助我改善绩效表现
我的经理不会随便插手我的工作（例如介入不应由其负责的细节问题上）
我的经理会从人性角度出发体谅我
我的经理使团队将注意力集中在最重要的目标结果/工作成果之上
我的经理定期分享他/她的上级经理和领导给出的相关信息
我的经理与我就过去6个月的职业发展情况进行过有意义的探讨
我的经理会明确向团队说明目标
我的经理具备高效管理团队所需的专业技术能力（比如，技术部门会编写代码，财务部门懂会计学）
我愿意向其他谷歌人推荐我的经理
=blue 【注】通过调查问卷搜集反馈从而进行改进，是一个重要方法
  
谷歌工作法则：管理团队的两端-最优员工和最差员工
助力有难处的员工
将最优秀的人放在显微镜下观察
利用调查和检查清单寻找真相，推动员工学习
与人分享员工对你的反馈意见，以身作则采取行动解决问题，身先示范
=blue 【注】帮助最差员工进行提高，观察最优员工进行培训学习

# 打造学习型组织
-------------------------
“泰格，你半夜3点在这儿打球干嘛？
“北加利福利亚不怎么下雨，难得有这样的机会在雨中练习。”
后来他成为历史上最成功的高尔夫球手之一。
传统观点认为，要成为某一方面的专家需要一万小时的练习。
埃里克森则发现重要的不仅仅是你投入多少时间学习，还有如何使用这些时间。
在某一领域精熟的人，学习的方法都有异于常人。
他们将活动分解成细小的动作，比如连续数小时在雨中练习同一种击球动作，不断重复。
每一次，他们都会观察效果，做微小的--几乎难以察觉的调整，逐步改进。
埃里克森将这种方式称作刻意练习：有意重复类似的小任务，即时反馈、修正和实验。
=blue 【注】刻意练习，不仅仅是重复那么简单，需要反馈和实验，学习和提升
  
每次拜访客户之前，他都会问我：
你这次会议的目标是什么？
你觉得每位客户会怎样回应？
你打算如何引入一个难解决的话题？
开完会回来又会问一些问题：
你采用的方式有效吗？
你学到了什么？
下次你想试试哪些新的手段？
=blue 【注】即时反馈，持续提高
  
奥本海默说过“最好的学习方式是教导他人”
=blue 【注】要教好学生，需要深入思考教学内容，用简洁典雅的方式将知识传递给他人。所以让员工做老师也非常有价值
  
谷歌工作法则：打造学习型组织
进行可以练习：将课程分成易于消化的小块，给出明晰的反馈意见，并不断重复这个过程
请最优秀的员工教学
只在已经证明能够改变员工行为的课程上进行投入
=blue 【注】学习是最大的投资

# 不公平薪酬
-------------------------
我们将现金奖励改为体验奖励，因为现金挥霍很容易从记忆中消失。
非现金奖励，不管是一些体验还是礼品，都能够激发情感反应。
