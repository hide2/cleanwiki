=blue 【注】即便不滥用权力，而只是滥用影响力，也是不对的
  
仔细观察一下，参加会议者陆续到场，他们会刻意将领导的座位空出来。
=blue 【注】不自觉的微妙行为创造了等级制度
打破等级，让员工像主人翁一样行动，而不是雇员
  
授权于群众的第一步就是要保证人们能够安全地发表意见
=blue 【注】要让团队敢说话，经理的权力越小，团队的创新范围越大
  
谷歌只有4个明确的级别划分：普通员工、经理、主管、高管
在谷歌，领导地位与头衔并不划等号
最高级的高管和新员工的福利一样
消除地位象征，象征符号和故事很重要，如谷歌CFO穿牛仔裤骑自行车的照片，而不是西装公文包
福特总统的狗在总统办公室上方便了一下，一位海军服务员立刻冲上去做清理，福特说：“走开，我来清理。自己的狗要自己管，不应该让别人收拾。”
时刻把公司的价值观放在醒目位置，否则人的本性就会不可避免的显露出来
=blue 【注】有了钱或有了头衔，就开始膨胀，这是劣根性的一种体现。项目奖金激励，并不是组长就一定比普通员工奖金多，规则上消除等级制度。时刻对自身行为和团队准则保持敏感，将无等级和无地位制度印入价值观
  
依靠数据而不是根据经理的想法做决定
我们利用数据和证据，来规避流言、偏见和旧式的刚愎自用
=blue 【注】不用浪费时间争论，做个测试就好
  
Googlegeist已成为我们公司重大变革的发源端，比如改变了我们对公司管理的看法，公司薪酬理念的彻底变革等
=blue 【注】员工成为主人翁，去一起改变公司。将员工即联合创始人印入价值观
  
经理们忽略的是，每次他们放弃一些控制权，就可以为团队创造一次提升的机会，也给自己节省出更多时间应对新的挑战
找出某个令你的团队感到沮丧的领域，让他们改变现状。如果有限制，比如时间或资金有限制，就告诉他们。要对员工透明，在塑造团队或公司的过程中给他们发言权。你会惊讶于他们的成就。
=blue 【注】授权很好的实践方法
  
谷歌工作法则：授权于员工
消除地位象征
依靠数据而不是根据经理的想法做决定
