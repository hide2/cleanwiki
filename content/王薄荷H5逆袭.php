其余程序，撇开法律风险不谈，如果你具备开发的能力，可以找我私聊，我这可以提供一些小流量来让你测试转化。
  
- # 王老师，回收最高的H5游戏类型是什么？
`目前所有的数据测下来，具备ip的一些重度游戏转化最好，传奇类就是，但是同质产品很多。反而你可以针对粉丝流量来做对应的产品，好比宠物类，原本号主就是靠着流量主收益，但是接入我的宠物休闲游戏，谁能想到每个月的收益近10万。`
  
