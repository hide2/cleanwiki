- # 请问一下王老师，公众号的运营您有一些理解吗？
公众号的运营，我从粉丝获取的角度谈一点，很多被人忽略的流量，好比微信的自然搜索量，之前我帮朋友整理了一些微信指数比较高的词，好比说钢琴谱这个词，每天的自然关注就有近200，但是做的人很少，抓住某个刚需的细分市场来做，其实获取粉丝不会很难，希望对你有帮助
  
- # 除了深度和入口之外，和APP游戏相比，h5有核心区别么？还有QQ能推h5么？
我尝试过用qq公众号来推，效果不是特别好，但是qq空间是一个被很多人遗忘的掘金地，我有一个朋友目前就是从事这快，利用一些引流手段，我说一点，好比说之前那种“鹿晗qq号54321，大家速加”利用记忆成本低的qq号来引流，再引流朋友圈，pv过千万，淘客跟h5游戏的变现都风生水起
  
- # 王老师，我还停留在站群做流量，但是现在越来越难，这个方式还值得继续研究吗？假如转型的话，应该怎么来选定方向？
站群现在的问题是，好的程序你很难找，好比前阵子流行的寄生虫，其实市面上的程序都是骗钱的，但是很多人还是会去购买，并且现在做站群，新域名已经满足不了，必须用备案甚至事业单位备案域名去做，成本很高，除非你的转化收益很高，否则我个人不建议继续去做
  
- # 请问王老师，现在找主播带量效果怎么样，谢谢
`top的主播带来的收益是爆炸级别的，但是合作门槛很高，要牺牲很多东西。中小主播以数量取胜是一个制胜点。`
  
